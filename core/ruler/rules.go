// fofaCheck fofa规则检查
func (r *Rule) fofaCheck(resp http.Response) (found bool, name string) {

	if strings.Contains(r.fofa, "||") && !strings.Contains(r.fofa, "&&") && !strings.Contains(r.fofa, "(") {
		// 满足一个条件即可的情况
		for _, x := range strings.Split(r.fofa, "||") {
			if checkRule(r.Name, x, resp) {
				return true, r.Name
			}
		}
	} else if !strings.Contains(r.fofa, "||") && !strings.Contains(r.fofa, "&&") && !strings.Contains(r.fofa, "(") {
		// 只有一个条件的情况
		//logger.Debugf("[-] Current Rule: %#v", r)
		if checkRule(r.Name, r.fofa, resp) {
			return true, r.Name
		}

	} else if strings.Contains(r.fofa, "&&") && !strings.Contains(r.fofa, "||") && !strings.Contains(r.fofa, "(") {
		// 需要同时满足条件的情况
		num := 0
		rule := strings.Split(r.fofa, "&&")
		for _, x := range rule {
			if checkRule(r.Name, x, resp) {
				num += 1
			}
		}
		if num == len(rule) {
			return true, r.Name
		}

	} else {
		// 与条件下存在并条件: 1||2||(3&&4)
		rule := rBracket.FindStringSubmatch(r.fofa)
		if len(rule) == 0 {
			logger.Errorf("与条件下存在并条件: 1||2||(3&&4) have a error, Current key: %s", r.fofa)
			return false, ""
		}
		if strings.Contains(rule[1], "&&") {
			for _, x := range strings.Split(r.fofa, "||") {
				if strings.Contains(x, "&&") {
					num := 0
					_rule := strings.Split(x, "&&")
					for _, j := range _rule {
						if checkRule(r.Name, j, resp) {
							num += 1
						}
					}
					if num == len(_rule) {
						return true, r.Name
					}
				} else {
					if checkRule(r.Name, x, resp) {
						return true, r.Name
					}
				}
			}
		} else {
			// 并条件下存在与条件： 1&&2&&(3||4)
			_rule := strings.Split(r.fofa, "&&")
			num := 0
			for _, x := range _rule {
				if strings.Contains(x, "||") {
					for _, j := range strings.Split(x, "||") {
						if checkRule(r.Name, j, resp) {
							num += 1
							break
						}
					}
				} else {
					if checkRule(r.Name, x, resp) {
						num += 1
					}
				}
			}
			if num == len(_rule) {
				return true, r.Name
			}
		}
	}

	return false, ""
}

func checkRule(name string, key string, resp http.Response) bool {
	if strings.Contains(key, "server=") ||
		strings.Contains(key, "banner=") ||
		strings.Contains(key, "protocol=") ||
		strings.Contains(key, "type=") ||
		strings.Contains(key, "port=") {
		return false
	}
	if strings.Contains(key, "title=") {
		rule := rTitle.FindStringSubmatch(key)
		if len(rule) == 0 {
			logger.Errorf("rTitle have a error, Current key is : %s", key)
		}
		_rule := strings.ToLower(rule[1])
		if strings.Contains(resp.Title, _rule) {
			return true
		}

	} else if strings.Contains(key, "body=") {
		rule := rBody.FindStringSubmatch(key)
		if len(rule) == 0 {
			logger.Errorf("rBody have a error, Current key is : %s", key)
		}
		_rule := strings.ToLower(rule[1])
		if strings.Contains(resp.Body, _rule) {
			return true
		}
	} else {
		rule := rHeader.FindStringSubmatch(key)
		if len(rule) == 0 {
			logger.Errorf("rHeader have a error, Current name is : %s, key : %s", name, key)
		}
		_rule := strings.ToLower(rule[1])
		if strings.Contains(resp.Header, _rule) {
			return true
		}
	}
	return false
}

